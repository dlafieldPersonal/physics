#include "Pobj.h"

using namespace std;

/***************************************************/
		
Pobj::Pobj(GLfloat x, GLfloat y)
{ 
	xCoord = x;
	yCoord = y;

} /* constructer */

/***************************************************/

Pobj::Pobj()
{ 
	xCoord = 0.5;
	yCoord = 0.5;

} /* constructer */

/***************************************************/

Pobj::~Pobj()
{ }

/***************************************************/

GLfloat Pobj::getXCoord() const { return xCoord; }
GLfloat Pobj::getYCoord() const { return yCoord; }
void Pobj::setXCoord(GLfloat x) { xCoord = x; }
void Pobj::setYCoord(GLfloat x) { yCoord = x; }

